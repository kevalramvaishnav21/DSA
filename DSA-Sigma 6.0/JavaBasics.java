import java.util.*;


public class JavaBasics {
    public static void main(String[] args) {
        Scanner SC = new Scanner(System.in);
        int n = SC.nextInt();
        int sum =0;


        int i = 1;
        while(i<=n){
            sum = sum + i ;
            i++;

        }

        System.out.println(sum);
        }

        
           

        
        
    }
